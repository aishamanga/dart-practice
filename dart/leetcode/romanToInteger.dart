int convert(String letter){
  switch (letter) {
    case "M":
      return 1000;
    case "CM":
      return 900;
    case "CD":
      return 400;
    case "C":
      return 100;
    case "XC":
      return 90;
    case "XL":
      return 40;
    case "X":
      return 10;
    case "IX":
      return 9;
      case "V":
      return 5;
    case "IV":
      return 4;
    case "I":
      return 1;

    default:return 0;
  }
}

